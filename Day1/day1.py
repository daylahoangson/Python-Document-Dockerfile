# Số nguyên (Integer)
so_nguyen = 10
print("Số nguyên:", so_nguyen, type(so_nguyen))

# Số thực (Float)
so_thuc = 3.14
print("Số thực:", so_thuc, type(so_thuc))

# Số phức (Complex)
so_phuc = 2 + 3j
print("Số phức:", so_phuc, type(so_phuc))

# Chuỗi (String)
chuoi = "Xin chào Python"
print("Chuỗi:", chuoi, type(chuoi))

# Boolean (True/False)
boolean_dung = True
boolean_sai = False
print("Boolean đúng:", boolean_dung, type(boolean_dung))
print("Boolean sai:", boolean_sai, type(boolean_sai))

# List (Danh sách - có thể thay đổi)
danh_sach = [1, 2, 3, "Python", True]
print("List:", danh_sach, type(danh_sach))

# Tuple (Bộ giá trị - không thay đổi)
bo_gia_tri = (1, 2, 3, "Python")
print("Tuple:", bo_gia_tri, type(bo_gia_tri))

# Set (Tập hợp - không trùng lặp, không có thứ tự)
tap_hop = {1, 2, 2, 3, "Python"}
print("Set:", tap_hop, type(tap_hop))

# Dictionary (Từ điển - cặp key-value)
tu_dien = {"ten": "Python", "nam": 1991, "dang_hoat_dong": True}
print("Dictionary:", tu_dien, type(tu_dien))

